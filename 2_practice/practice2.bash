#!/bin/bash

# Дозволяємо bash бачити приховані файли та папки (ті, що починаються з крапки)
shopt -s dotglob

# Встановлюємо директорії: беремо з аргументів або використовуємо стандартні
DIR1=${1:-/home}
DIR2=${2:-/var/log}

# Функція для підрахунку та виведення топ-10 об'єктів
get_top_10() {
    local target_dir=$1
    echo "=================================================="
    echo "Топ-10 папок та файлів у: $target_dir (у байтах)"
    echo "=================================================="
    
    # Перевіряємо, чи існує директорія
    if [ ! -d "$target_dir" ]; then
        echo "Помилка: Директорія $target_dir не існує або недоступна."
        echo ""
        return
    fi

    # Використовуємо du:
    # -s: виводить лише загальний розмір для кожного вказаного файлу/папки
    # -b: виводить розмір у байтах	
    # 2>/dev/null: приховує повідомлення про помилки (наприклад, відмову в доступі)
    # sort -nr: сортує числа (-n) у зворотньому/спадному порядку (-r)
    # head -n 10: залишає лише перші 10 рядків
    du -sb "$target_dir"/* 2>/dev/null | sort -nr | head -n 10
    
    echo ""
}

# Викликаємо функцію для обох директорій
get_top_10 "$DIR1"
get_top_10 "$DIR2"
