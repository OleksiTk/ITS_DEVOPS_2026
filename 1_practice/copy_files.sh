#!/bin/bash


FORMAT=$1

# Перевіряємо, чи передано параметр
if [ -z "$FORMAT" ]; then
    echo "Помилка: Не вказано формат файлів."
    echo "Використання: $0 '<формат>' (наприклад, '*.txt')"
    exit 1
fi

# Створюємо нову директорію, якщо вона ще не існує
mkdir -p /tmp/newdir

# Копіюємо файли вказаного формату в нову директорію. 
# Використовуємо змінну без лапок, щоб Bash розгорнув зірочку (wildcard).
cp $FORMAT /tmp/newdir/ 2>/dev/null

# Перевіряємо успішність виконання команди cp
if [ $? -eq 0 ]; then
    echo "Файли формату $FORMAT успішно скопійовано до /tmp/newdir"
else
    echo "Файлів формату $FORMAT не знайдено в поточній директорії."
fi